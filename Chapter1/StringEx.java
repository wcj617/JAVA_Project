package Chapter1;

class StringEx{
    public static void main (String[] args) {
        String name = "Ja" + "Va";
        String str  = name + 8.0;

        System.out.println(name);
        System.out.println(str);
        System.out.println(7 + " ");
        System.out.println(7);
        System.out.println(" " + 7);
        System.out.println(7 + "");
        System.out.println("" + 7);
        System.out.println(true + "");
        System.out.println(true);
        System.out.println(7 + 7 + "");
        System.out.println("" + 7 + 7);
    }
}